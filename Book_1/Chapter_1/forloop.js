// 1. EVEN NUMBERS (using += 2)
// Start at 0, loop while i is less than or equal to 10, increase by 2 each time to get even numbers
for (var i = 0; i <= 10; i += 2) {
  // Print the current even number
  console.log(i);
}

// 2. ODD NUMBERS (using += 2)
// Start at 1, loop while i is less than or equal to 10, increase by 2 each time to get odd numbers
for (var i = 1; i <= 10; i += 2) {
  // Print the current odd number
  console.log(i);
}

// 3. COUNTING DOWN BY 10 (using -= 10)
// Start at 100, loop while i is greater than or equal to 0, decrease by 10 each time
for (var i = 100; i >= 0; i -= 10) {
  // Print the current number in the countdown
  console.log(i);
}

// 4. PRE-INCREMENT (++i)
// Start at 0, loop while i is less than 5, increment i by 1 before using it in each iteration
for (var i = 0; i < 5; ++i) {
  // Print the current value of i
  console.log(i);
}


// Loop starting from 5, counting down to 2 (i > 1), decreasing i by 1 each time
for (var i = 5; i > 1; --i) {
  // Print the current value of i
  console.log(i);
}
